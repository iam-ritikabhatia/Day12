// String ProtoTyping is To Modify the String in its own Way

/************charAt***************/
// It is used to find out the character at any index number

// Syntax

// console.log(string.charAt(index number))

let myName = "Ramesh Sharma"
console.log(myName.charAt(5)) // It returns the character at the index number 5

let Name01 = "Ramesh Sharma"
console.log(Name01[5]) // this will also return the character at index number, this will treat Name01 as an array and [5] means the character at index number 5

/**a String is treated like an array in JavaScript and Python Language */